import UIKit

extension UIColor {
    
    static func elementPrimaryBackgroundColor() -> UIColor {
        return UIColor.white;
    }
    
    static func primaryBackgroundColor() -> UIColor {
        return UIColor(red: 228/255.0, green: 228/255.0, blue: 228/255.0, alpha: 1.0)
    }
    
    static func tertiaryColor() -> UIColor {
        return UIColor(red: 132/255.0, green: 137/255.0, blue: 142/255.0, alpha: 1.0)
    }

    
}
